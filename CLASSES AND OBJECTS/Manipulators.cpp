// manipulators (endl,setw)
#include<iostream>
#include<iomanip>           // for setw manipulator
using namespace std;
int main(){
	cout<<setw(10)<<"Name :"<<setw(15)<<"Arpit"<<endl;
	cout<<setw(10)<<"Rollno :"<<setw(15)<<"16"<<endl;
	cout<<setw(10)<<"Marks :"<<setw(15)<<"20"<<endl;
	cout<<setw(10)<<"Name :"<<setw(15)<<"Patel"<<endl;
	cout<<setw(10)<<"Rollno :"<<setw(15)<<"116"<<endl;
	cout<<setw(10)<<"Marks :"<<setw(15)<<"30"<<endl;
	return 0;
}
